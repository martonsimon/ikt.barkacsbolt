function loadProduct() {
  var queryParams = getQueryParams();
  var itemID = queryParams ? queryParams.item || "0" : "0";

  var item = getItemById(itemID * 1);

  var productWrapper = document.getElementById(product_wrapper_id);
  productWrapper.innerHTML = createProductHTML(item);

  var categoryNavBack = document.getElementById("category-nav-link");
  categoryNavBack.innerHTML = `${getCategoryById(item.category_id).name}`;
  categoryNavBack.href = `pages/products.html?category=${item.category_id}`;
}
function addToCart() {
  var amountInp = document.getElementById("amount");
  var amount = amountInp.value;
  if (amount) {
    var queryParams = getQueryParams();
    var itemID = queryParams ? queryParams.item || "0" : "0";

    var item = getItemById(itemID * 1);
    addItemToCart(item, amount);
    console.log("Add to cart");
    console.log(amount);
  } else {
    alert("Adj meg egy mennyiséget!");
  }
}

